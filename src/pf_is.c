int		is_format_spec(char c)
{
	return (c == 'c' || c == 'i' || c == 'd' || c == 's' || c == 'u' ||\
			c == 'p' || c == 'x' || c == 'X' || c == '%');
}

int		is_flag(char c)
{
	return (c == '-' || c == '*' || c == '.' || (c >= '0' && c <= '9'));
}
